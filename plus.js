function plus(num1,num2){
    let result;
    result=num1+num2;
    return result
}
function minus(num1,num2){
    let result;
    result=num1-num2;
    return result
}
function divide(num1,num2){
    let result;
    result=num1/num2;
    return result
}
function multiply(num1,num2){
    let result;
    result=num1*num2;
    return result
}